# OPERASI BITWISE
a=6
b=4

# SHIFT (left and right)
print("SHIFT: ")
c=a<<2
print("binary dari",a,":" ,format(a,'08b'))
print("binary dari",b,":" ,format(b,'08b'))
print("binary dari",c,":" ,format(c,'08b'))
print("===================================")
c=b<<3
print("binary dari",c,":" ,format(c,'08b'))
print("===================================")
c=a>>3
print("binary dari",c,":" ,format(c,'08b'))
print("===================================")
c=b>>2
print("binary dari",c,":" ,format(c,'08b'))
print("\n")

# AND(&), OR(|), XOR(^), INVERT(~)
print("BITWISE AND(&) : ")
